# 7.1 数学函数
import math      # 导入math模块

# 7.1.1 round函数：四舍五入
x = 2.9
print(round(x))
'''
运行结果：3
'''

# 7.1.2 abs函数：获得绝对值
print(abs(-2.9))
'''
运行结果：2.9
'''

print('\n')

# 7.1.3 math模块（导入模块的语句见第二行）

# 7.1.4 ceil方法：不小于x的最小整数
print(math.ceil(2.9))
'''
运行结果：3
'''

# 7.1.5 floor方法：不大于x的最大整数
print(math.floor(2.9))
'''
运行结果：2
'''
