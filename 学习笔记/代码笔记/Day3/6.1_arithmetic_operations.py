# 6.1 算术运算
# 6.1.1 基本算数运算符
print(10+3)    #1）加法运算
print(10-3)    #2）减法运算
print(10*3)    #3）乘法运算
print(10/3)    #4）除不尽时可以得到浮点数结果的除法运算
print(10//3)   #5）除不尽时保留整数的除法运算
print(10%3)    #6）取余运算
print(10**3)   #7) 幂运算
