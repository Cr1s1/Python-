# 6.2 运算符优先级

# 6.2.1 优先级顺序
x = 10 + 3 * 2
print(x)
x = 10 + 3 * 2 ** 2
print(x)
'''
运行结果：
----------
16
22
==========
执行顺序：（幂运算、）乘法、加法
'''
# 6.2.2 可以用括号改变先后顺序
x = (10 + 3) * 2 ** 2
print(x)
'''
运行结果：
----------
52
----------
执行顺序：加法、幂运算、乘法
结论：括号的优先级最高
'''

